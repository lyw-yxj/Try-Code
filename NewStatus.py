# 元组（Tuple）
# 元组是不可变的序列，一旦创建，其内部元素就不能被修改。
# 元组使用小括号()来定义，而列表使用方括号[]。
# 元组通常用于存储那些不应该被改变的数据集，如函数的返回值、数据库的查询结果等。
# 元组的设计初衷是为了提供一种更轻量级、更安全的数据结构，不可变性保证了数据的完整性，防止在程序运行过程中被意外修改。


